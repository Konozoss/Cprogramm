
 #include <stdio.h>

int main() {
    int num1 = 5, num2 = 10, sum;

    // جمع الأرقام
    sum = num1 + num2;

   // عرض النتيجة
    printf("The sum of %d and %d is: %d\n", num1, num2, sum);

    return 0;
}
/* شرح مراحل ترجمة الكود:

مرحلة التحليل المعجمي: تقسيم الكود إلى Tokens.
Tokens: [int, a, =, 10, ;, int, b, =, 5, ;]

مرحلة التحليل النحوي: التحقق من القواعد النحوية.
شجرة التحليل:
   PROGRAM
   ├── DECLARATION (int a = 10;)
   ├── DECLARATION (int b = 5;)
   └── ASSIGNMENT (sum = a + b;)

مرحلة التحليل الدلالي: التحقق من العمليات ومعانيها.
التحقق: 'a' و 'b' من النوع int، العملية (a + b) صحيحة.

مرحلة إنشاء الكود الوسيط:
t1 = 10
t2 = 5
t3 = t1 + t2
sum = t3

مرحلة تحسين الكود:
اختزال الكود الوسيط:
sum = 10 + 5
sum = 15

مرحلة توليد الكود النهائي:
MOV R0, #10    ; تحميل القيمة 10
MOV R1, #5     ; تحميل القيمة 5
ADD R2, R0, R1 ; جمع القيمتين
STO R2, sum    ; تخزين النتيجة */
